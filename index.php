function replaceSpaces($str) {
    $translation = '<br>';

    for ($i = 0; $i < mb_strlen($str); $i += 1) {
        $char = mb_substr($str, $i, 1);
        if ($char===' ') {
            $char='_';
        }
        $translation .= $char;
    }
    echo $translation;
}

