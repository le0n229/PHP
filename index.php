function translate($str)
{
    $dictionary = [
        'а' => 'a', 'б' => 'b', 'в' => 'v',
        'г' => 'g', 'д' => 'd', 'е' => 'e',
        'ё' => 'e', 'ж' => 'zh', 'з' => 'z',
        'и' => 'i', 'й' => 'y', 'к' => 'k',
        'л' => 'l', 'м' => 'm', 'н' => 'n',
        'о' => 'o', 'п' => 'p', 'р' => 'r',
        'с' => 's', 'т' => 't', 'у' => 'u',
        'ф' => 'f', 'х' => 'h', 'ц' => 'c',
        'ч' => 'ch', 'ш' => 'sh', 'щ' => 'sch',
        'ь' => '\'', 'ы' => 'y', 'ъ' => '\'',
        'э' => 'e', 'ю' => 'yu', 'я' => 'ya'
    ];

    $translation = '<br>';

    for ($i = 0; $i < mb_strlen($str); $i += 1) {
        $char = mb_substr($str, $i, 1);
        $big = false;
        if ($char === mb_strtoupper($char)) {
            $big = true;
        }
        if (preg_match("/[а-яё]/i", $char)) {
            if ($big){
                $newchar = $dictionary[mb_strtolower($char)];
                $translation .= mb_strtoupper($newchar);
            } else {
            $newchar = $dictionary[$char];
            $translation .= $newchar;
            }
        } else {
            $translation .= $char;
            continue;
        }

//        $translation.=$dictionary[$str[$i]];
    }

    echo $translation;

}

